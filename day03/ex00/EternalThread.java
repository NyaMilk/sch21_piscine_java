public class EternalThread extends Thread {
    private String name;

    private int count;

    public EternalThread(String name, int count) {
        this.name = name;
        this.count = count;
    }

    @Override
    public void run() {
        for (int i = 0; i < count; i++) {
            System.out.println(name);
        }
    }
}